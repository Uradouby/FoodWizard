package com.example.foodwizard

class SimpleMeal(
    name:String,
    fat:Int,
    carb:Int,
    protein:Int,
    calory:Int,
    imagePath:String
)
{
    val Name: String =name
    val Fat:Int=fat
    val Carb:Int=carb
    val Protein:Int=protein
    val Calory:Int=calory
    val ImagePath:String =imagePath
}